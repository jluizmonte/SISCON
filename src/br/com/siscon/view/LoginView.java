    private void entrar() {
        //seta no modelusuario a senha e o login digitados nos campos pelo usuário
        usuarioModel.setUsuLogin(jtfLogin.getText());
        usuarioModel.setUsuSenha(String.valueOf(jtfSenha.getPassword()));
        /*
        chama o metódo para validar o usuário, verificar senha e efetuar o login
        se o login e senha estiverem corretos, o form principal é chamado
        se houver algum tipo de erro, o usuário recebera a mensagem de erro
         */

        if (controllerUsuario.getValidarUsuarioDAO(usuarioModel)) {
            usuarioModel = controllerUsuario.getUsuarioDAO(jtfLogin.getText());
            SessaoUsuarioModel.codigoUsuario = usuarioModel.getIdUsuario();
            SessaoUsuarioModel.nomeUsuario = usuarioModel.getUsuNome();
            SessaoUsuarioModel.loginUsuario = usuarioModel.getUsuLogin();
//            JOptionPane.showMessageDialog(this, "Bem-vindo de volta " + usuarioModel.getUsuNome() + "!", "Alerta", JOptionPane.WARNING_MESSAGE);
            new PrincipalView().setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Usuário ou senha inválidos!", "Aviso", JOptionPane.WARNING_MESSAGE);
            jtfLogin.requestFocusInWindow();
        }
    }

